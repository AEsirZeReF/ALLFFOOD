package Entidades;

public class Chef {
private int cod_chef;
private String usuario;
private String password;
private String nom_chef;
private String ape_chef;
private int edad;
private String celular;
private String dieccion;
private double saldo_chef;
public int getCod_chef() {
	return cod_chef;
}
public void setCod_chef(int cod_chef) {
	this.cod_chef = cod_chef;
}
public String getUsuario() {
	return usuario;
}
public void setUsuario(String usuario) {
	this.usuario = usuario;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getNom_chef() {
	return nom_chef;
}
public void setNom_chef(String nom_chef) {
	this.nom_chef = nom_chef;
}
public String getApe_chef() {
	return ape_chef;
}
public void setApe_chef(String ape_chef) {
	this.ape_chef = ape_chef;
}
public int getEdad() {
	return edad;
}
public void setEdad(int edad) {
	this.edad = edad;
}
public String getCelular() {
	return celular;
}
public void setCelular(String celular) {
	this.celular = celular;
}
public String getDieccion() {
	return dieccion;
}
public void setDieccion(String dieccion) {
	this.dieccion = dieccion;
}
public double getSaldo_chef() {
	return saldo_chef;
}
public void setSaldo_chef(double saldo_chef) {
	this.saldo_chef = saldo_chef;
}


}
